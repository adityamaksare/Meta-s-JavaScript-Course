// For Loop

/*

i is a counter variable

for loops constitutes of:
(counter variable value; condition; increment/updation)

*/



// Increment
for (var i = 1; i <= 3; i++)   { 
    console.log(i);  
}
console.log("Go");


// Decrement 
for (var i = 10; i > 0; i--) {
    console.log(i);
}
console.log("Happy New Year!");